    public static void vlUpdate(){
        boolean teste;
        String a, t;
        listC.removeAll();
        a = findC.getText();
        for(Compra obj : f.getLC()){
            t = obj.getCode();
            teste = false;
            for(int i = 0; i < a.length(); i++){
                if(a.charAt(i) != t.charAt(i) || a.length() > t.length()){
                    teste = true;
                    break;
                }
            }
            if(!teste) listC.add(t);
        }
    }

